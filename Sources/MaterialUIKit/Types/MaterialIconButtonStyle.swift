import SwiftUI

/// Represens different styles for the ``MaterialIconButton``.
@frozen
public enum MaterialIconButtonStyle {
    /// A circular button with an secondary style.
    case secondary
    
    /// A circular button with an elevated style.
    case elevated
    
    /// A circular button with a filled style.
    case filled
    
    /// A circular button with a tonal style.
    case tonal
}
