import SwiftUI

/// The `MUIConstants` class encapsulates various constants that are utilized throughout the package.
@available(iOS 15.0, *)
internal struct MUIConstants {
    
    private init() {}
    
    /// A CGFloat value representing the corner radius used in MaterialUIKit components.
    internal static var cornerRadius: CGFloat = 25
    
    ///  A CGFloat value representing the vertical content padding used in MaterialUIKit components.
    internal static var verticalContentPadding: CGFloat = 15
    
    /// A CGFloat value representing the inside margin used in MaterialUIKit components.
    internal static var insideMargin: CGFloat = 15
}
